public class Calculator {

    public int addTwoNumbers(int num1, int num2) {
        int sum = num1 + num2;
        return sum;
    }
    public int productTwoNumbers(int num1, int num2) {
        int product = num1 * num2;
        return product;
    }
    public int divideTwoNumbers(int num1, int num2) {
        int division = num1 / num2;
        return division;
    }
}
